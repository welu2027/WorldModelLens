"""LatentTrajectory — an ordered sequence of LatentState objects.

A ``LatentTrajectory`` represents a complete (or partial) episode in latent
space — either a *real* trajectory reconstructed from environment observations,
or an *imagined* rollout generated entirely inside the world model.

It exposes stacked tensor sequences for all per-step quantities and provides
high-level analysis helpers such as :meth:`surprise_peaks`, :meth:`fork_at`,
and :meth:`slice`.
"""

from __future__ import annotations

import dataclasses
from dataclasses import dataclass, field
from typing import Any

import torch

from world_model_lens.core.latent_state import LatentState


@dataclass(eq=False)
class LatentTrajectory:
    """Ordered sequence of :class:`LatentState` objects forming one episode.

    Parameters
    ----------
    states:
        Ordered list of :class:`LatentState` objects, one per time-step.
        All states must share the same ``h_t`` size, ``n_cat``, and ``n_cls``.
    env_name:
        Identifier of the environment (e.g. ``"CartPole-v1"``).
    episode_id:
        Unique episode identifier (e.g. a UUID or ``"run_0042/ep_007"``).
    imagined:
        ``True`` if this trajectory was generated by the world model without
        real environment interaction.
    fork_point:
        Step index (inclusive) at which this trajectory was forked from a
        parent trajectory.  ``None`` for trajectories that began at t=0.
    metadata:
        Arbitrary key-value annotations (e.g. seed, agent version).

    Examples
    --------
    >>> import torch
    >>> from world_model_lens.core import LatentState, LatentTrajectory
    >>> states = [
    ...     LatentState(torch.randn(512), torch.randn(32, 32), torch.randn(32, 32), t)
    ...     for t in range(10)
    ... ]
    >>> traj = LatentTrajectory(states, env_name="CartPole-v1", episode_id="ep_000")
    >>> traj.length
    10
    >>> traj.h_sequence.shape
    torch.Size([10, 512])
    >>> traj.kl_sequence.shape
    torch.Size([10])
    """

    # ------------------------------------------------------------------
    # Required fields
    # ------------------------------------------------------------------
    states: list[LatentState]
    """Ordered per-step latent states."""

    env_name: str
    """Environment identifier."""

    episode_id: str
    """Unique episode identifier."""

    # ------------------------------------------------------------------
    # Optional / flag fields
    # ------------------------------------------------------------------
    imagined: bool = False
    """Whether the trajectory is an imagined rollout."""

    fork_point: int | None = None
    """Step index from which this trajectory was forked, or ``None``."""

    metadata: dict[str, Any] | None = field(default=None)
    """Arbitrary extra annotations."""

    # ------------------------------------------------------------------
    # Properties — scalars / sequences
    # ------------------------------------------------------------------

    @property
    def length(self) -> int:
        """Number of time-steps in the trajectory.

        Returns
        -------
        int
            ``len(self.states)``.
        """
        return len(self.states)

    @property
    def h_sequence(self) -> torch.Tensor:
        """Stacked recurrent hidden states, shape ``(T, d_h)``.

        Returns
        -------
        torch.Tensor
            Tensor formed by stacking :attr:`LatentState.h_t` over all steps.

        Examples
        --------
        >>> traj.h_sequence.shape
        torch.Size([10, 512])
        """
        return torch.stack([s.h_t for s in self.states], dim=0)

    @property
    def z_posterior_sequence(self) -> torch.Tensor:
        """Stacked posterior categorical latents, shape ``(T, n_cat, n_cls)``.

        Returns
        -------
        torch.Tensor
            Tensor formed by stacking :attr:`LatentState.z_posterior` over all
            steps.
        """
        return torch.stack([s.z_posterior for s in self.states], dim=0)

    @property
    def z_prior_sequence(self) -> torch.Tensor:
        """Stacked prior categorical latents, shape ``(T, n_cat, n_cls)``.

        Returns
        -------
        torch.Tensor
            Tensor formed by stacking :attr:`LatentState.z_prior` over all
            steps.
        """
        return torch.stack([s.z_prior for s in self.states], dim=0)

    @property
    def kl_sequence(self) -> torch.Tensor:
        """Per-step KL divergence KL(posterior ‖ prior), shape ``(T,)``.

        Computes :attr:`LatentState.kl` for every step and stacks the results
        into a 1-D tensor.  Useful for identifying high-surprise moments in a
        trajectory.

        Returns
        -------
        torch.Tensor
            1-D tensor of non-negative scalars.

        Examples
        --------
        >>> traj.kl_sequence.mean()   # average surprise across episode
        tensor(...)
        """
        return torch.stack([s.kl for s in self.states], dim=0)

    @property
    def rewards_pred(self) -> torch.Tensor | None:
        """Predicted rewards stacked to shape ``(T,)``, or ``None`` if absent.

        Returns ``None`` if *any* state in the trajectory is missing
        ``reward_pred``, since a partially-populated sequence is ambiguous.

        Returns
        -------
        torch.Tensor | None
            1-D float tensor or ``None``.
        """
        if any(s.reward_pred is None for s in self.states):
            return None
        return torch.tensor(
            [s.reward_pred for s in self.states],  # type: ignore[arg-type]
            dtype=torch.float32,
        )

    @property
    def rewards_real(self) -> torch.Tensor | None:
        """Ground-truth rewards stacked to shape ``(T,)``, or ``None`` if absent.

        Returns
        -------
        torch.Tensor | None
            1-D float tensor or ``None``.
        """
        if any(s.reward_real is None for s in self.states):
            return None
        return torch.tensor(
            [s.reward_real for s in self.states],  # type: ignore[arg-type]
            dtype=torch.float32,
        )

    @property
    def actions(self) -> torch.Tensor | None:
        """Actions stacked to shape ``(T, *action_shape)``, or ``None`` if absent.

        Returns ``None`` if *any* step has ``action=None``.

        Returns
        -------
        torch.Tensor | None
            Tensor of shape ``(T,)`` or ``(T, d_a)`` depending on whether
            actions are scalars or vectors.  ``None`` if unavailable.
        """
        if any(s.action is None for s in self.states):
            return None
        return torch.stack(
            [s.action for s in self.states],  # type: ignore[union-attr]
            dim=0,
        )

    # ------------------------------------------------------------------
    # Methods
    # ------------------------------------------------------------------

    def surprise_peaks(self, threshold: float) -> list[int]:
        """Return step indices where the KL surprise exceeds *threshold*.

        Iterates over all states and collects indices where
        :attr:`LatentState.kl` > *threshold*.  Useful for identifying
        time-steps where the world model's prediction was most wrong.

        Parameters
        ----------
        threshold:
            Scalar cutoff value.  Steps with ``kl > threshold`` are returned.

        Returns
        -------
        list of int
            Sorted list of step indices (may be empty).

        Examples
        --------
        >>> peaks = traj.surprise_peaks(threshold=1.0)
        >>> print(peaks)
        [3, 7, 8]
        """
        return [
            s.timestep
            for s in self.states
            if s.kl.item() > threshold
        ]

    def slice(self, start: int, end: int) -> LatentTrajectory:
        """Return a sub-trajectory from step *start* (inclusive) to *end* (exclusive).

        The returned :class:`LatentTrajectory` shares the same ``env_name``
        and ``episode_id`` as the parent but with ``fork_point`` set to
        *start* (unless the parent already has a fork_point earlier than
        *start*, in which case the parent's fork_point is preserved if it is
        ``None``).

        Parameters
        ----------
        start:
            First step index to include (0-indexed).
        end:
            One past the last step to include.  Supports negative Python
            slice semantics (e.g. ``end=-1`` excludes the last step).

        Returns
        -------
        LatentTrajectory
            Sub-trajectory with ``length == end - start`` (after clamping).

        Raises
        ------
        IndexError
            If *start* is out of bounds or *start* >= *end*.

        Examples
        --------
        >>> sub = traj.slice(2, 7)
        >>> sub.length
        5
        >>> sub.states[0].timestep
        2
        """
        sliced = self.states[start:end]
        if not sliced:
            raise IndexError(
                f"Slice [{start}:{end}] produced an empty trajectory "
                f"(trajectory length = {self.length})."
            )
        return dataclasses.replace(
            self,
            states=sliced,
            fork_point=start,
            metadata={**(self.metadata or {}), "_sliced_from": self.episode_id},
        )

    def to_device(self, device: str | torch.device) -> LatentTrajectory:
        """Return a copy with all :class:`LatentState` tensors moved to *device*.

        Parameters
        ----------
        device:
            Target device string or :class:`torch.device` instance.

        Returns
        -------
        LatentTrajectory
            New :class:`LatentTrajectory` where every state has been moved to
            the requested device.

        Examples
        --------
        >>> traj_gpu = traj.to_device("cuda")
        >>> traj_gpu.h_sequence.device
        device(type='cuda', index=0)
        """
        return dataclasses.replace(
            self,
            states=[s.to_device(device) for s in self.states],
        )

    def fork_at(self, timestep: int) -> LatentTrajectory:
        """Create a new imagined trajectory starting from *timestep*.

        Returns a :class:`LatentTrajectory` consisting of the states from
        *timestep* onward (inclusive), with ``imagined=True`` and
        ``fork_point=timestep``.  This models the scenario where the world
        model takes over from a real observation at *timestep* and rolls out
        a counterfactual future.

        Parameters
        ----------
        timestep:
            The step index (inclusive) from which to fork.  Must satisfy
            ``0 <= timestep < self.length``.

        Returns
        -------
        LatentTrajectory
            Forked sub-trajectory.  The ``episode_id`` is suffixed with
            ``"_fork@<timestep>"`` to distinguish it from the parent.

        Raises
        ------
        IndexError
            If *timestep* is out of range.

        Examples
        --------
        >>> forked = traj.fork_at(5)
        >>> forked.imagined
        True
        >>> forked.fork_point
        5
        >>> forked.length
        5    # for a 10-step trajectory forked at step 5
        """
        if not (0 <= timestep < self.length):
            raise IndexError(
                f"fork_at({timestep}) is out of range for trajectory of "
                f"length {self.length}."
            )
        return dataclasses.replace(
            self,
            states=self.states[timestep:],
            imagined=True,
            fork_point=timestep,
            episode_id=f"{self.episode_id}_fork@{timestep}",
        )

    def __repr__(self) -> str:
        return (
            f"LatentTrajectory("
            f"env={self.env_name!r}, "
            f"id={self.episode_id!r}, "
            f"T={self.length}, "
            f"imagined={self.imagined}, "
            f"fork_point={self.fork_point})"
        )

    def __len__(self) -> int:
        return self.length

    def __iter__(self):
        return iter(self.states)

    def __getitem__(self, idx: int) -> LatentState:
        return self.states[idx]
